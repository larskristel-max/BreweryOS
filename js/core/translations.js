// ─── LANGUAGE PERSISTENCE ────────────────────────────────────────────────────
const LANGUAGE_STORAGE_KEY = 'brewos_language';
const LANGUAGE_OPTIONS = ['fr', 'nl', 'en', 'de'];
const DEFAULT_LANGUAGE = 'fr';

function getAccountLanguagePreference() {
  return null; // Future hook only
}

function getStoredLanguagePreference() {
  try {
    const value = localStorage.getItem(LANGUAGE_STORAGE_KEY);
    return LANGUAGE_OPTIONS.includes(value) ? value : null;
  } catch (err) {
    console.warn('[Language] Failed to read local preference', err);
    return null;
  }
}

function setStoredLanguagePreference(lang) {
  if (!LANGUAGE_OPTIONS.includes(lang)) return;
  try {
    localStorage.setItem(LANGUAGE_STORAGE_KEY, lang);
  } catch (err) {
    console.warn('[Language] Failed to store local preference', err);
  }
}

function resolveInitialLanguage() {
  const accountLang = getAccountLanguagePreference();
  if (LANGUAGE_OPTIONS.includes(accountLang)) return accountLang;
  const storedLang = getStoredLanguagePreference();
  if (LANGUAGE_OPTIONS.includes(storedLang)) return storedLang;
  return DEFAULT_LANGUAGE;
}

function hasSavedLanguagePreference() {
  return !!getAccountLanguagePreference() || !!getStoredLanguagePreference();
}

let currentLang = resolveInitialLanguage();

function t(key) {
  return (TRANSLATIONS[currentLang] && TRANSLATIONS[currentLang][key])
    || (TRANSLATIONS['en'] && TRANSLATIONS['en'][key])
    || key;
}

function setLanguage(lang) {
  if (!LANGUAGE_OPTIONS.includes(lang)) return;
  currentLang = lang;
  setStoredLanguagePreference(lang);
}

// legacy alias (used by settings logic)
function hasChosenLanguage() {
  return hasSavedLanguagePreference();
}
